function [M, S]= par_M_S(c)
    M1=[-20 -20 -10 -5;
    -10  -5  -5 -2;
     -5  -2  -2  0;
     -2   0   0  2;
      0   2   2  5;
      2   5   5 10;
      5  10  20 20];

  M2=[-20 -20 -10 -5;
      -10  -5  -4 -2;
       -4  -2  -1  0;
       -1   0   0  1;
        0   1   2  4;
        2   4   5 10;
        5  10  20 20];

  % conjuntos de salida
  S1=[-7 -5 -5 -3;
      -5 -3 -3 -1;
      -3 -1 -1  0;
      -1  0  0  1;
       0  1  1  3;
       1  3  3  5;
       3  5  5  7];

  S2=[-7 -5 -5 -4;
      -5 -4 -4 -3;
      -4 -3 -3  0;
      -3  0  0  3;
       0  3  3  4;
       3  4  4  5;
       4  5  5  7];
       
  if c == 1
    M = M1; S = S1;
  endif
  if c == 2
    M = M2; S = S1;
  endif
  if c == 3
    M = M1; S = S2;
  endif
  if c == 4
    M = M2; S = S2;
  endif 
  
  
endfunction
